		dateNow.val(now.toISOString().split("T")[0]);

		// Formatting and setting current time to the time input
		let hours = now.getHours().toString().padStart(2, "0");
		let minutes = now.getMinutes().toString().padStart(2, "0");
		let timeInput = $("#time-input");
		timeInput.val(hours + ":" + minutes);

		// Function to toggle icon and content of room and guest drop-downs
		function toggleIconAndContent() {
			let gicon = $(".guest-drop .arrow i");
			let gcontent = $(".guest-drop-content");
			let icon = $(".room-drop .arrow i");
			let content = $(".room-drop-content");

			if (gicon.hasClass("fa-angle-down")) {
				gicon.removeClass("fa-angle-down").addClass("fa-angle-up");
				icon.addClass("fa-angle-down").removeClass("fa-angle-up");
				gcontent.removeClass("inactive").addClass("active");
				content.removeClass("active").addClass("inactive");
			} else if (
				gicon.hasClass("fa-angle-up") &&
				icon.hasClass("fa-angle-down")
			) {
				gicon.removeClass("fa-angle-up").addClass("fa-angle-down");
				icon.removeClass("fa-angle-up").addClass("fa-angle-down");
				gcontent.removeClass("active").addClass("inactive");
				content.addClass("inactive");
			} else {
				gicon.removeClass("fa-angle-up").addClass("fa-angle-down");
				icon.removeClass("fa-angle-down").addClass("fa-angle-up");
				gcontent.removeClass("active").addClass("inactive");
				content.removeClass("inactive").addClass("active");
			}
		}

