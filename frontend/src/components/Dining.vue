			let icon = $(".room-drop .arrow i");
			let content = $(".room-drop-content");

			if (icon.hasClass("fa-angle-down")) {
				icon.removeClass("fa-angle-down").addClass("fa-angle-up");
				gicon.addClass("fa-angle-down").removeClass("fa-angle-up");
				content.removeClass("inactive").addClass("active");
				gcontent.removeClass("active").addClass("inactive");
			} else if (
				icon.hasClass("fa-angle-up") &&
				gicon.hasClass("fa-angle-down")
			) {
				icon.removeClass("fa-angle-up").addClass("fa-angle-down");
