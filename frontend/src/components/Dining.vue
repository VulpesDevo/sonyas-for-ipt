			var roomPlus = $(".room-plus");
			var roomNo = $(".room-no");
			var roomDisplay = $(".room");
			var maxRoom = 5;

			roomMinus.on("click", function () {
				var currentRoomNo = parseInt(roomNo.text());
				if (currentRoomNo > 1) {
					roomNo.text(currentRoomNo - 1);
					roomDisplay.text(roomNo.text());
				}
			});

			roomPlus.on("click", function () {
				var currentRoomNo = parseInt(roomNo.text());
				if (currentRoomNo < maxRoom) {
					roomNo.text(currentRoomNo + 1);
					roomDisplay.text(roomNo.text());
				}
			});

			// Additional adjustments for adult and child
			// ...
		});

